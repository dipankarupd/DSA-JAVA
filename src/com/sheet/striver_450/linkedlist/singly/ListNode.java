package com.sheet.striver_450.linkedlist.singly;


// implement the node

public class ListNode {

    int val;
    ListNode next;

    ListNode(){}

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
